import { Route, Redirect } from 'react-router';


const privateRoute = ({ children, ...rest }) => {
    // get auth some how from identity core
    let auth
    return (
        <Route
            {...rest}
            render={({ location }) =>
                auth.user ? (
                    children
                ) : (
                    <Redirect
                        to={{
                            pathname: "/login",
                            state: { from: location }
                        }}
                    />
                )
            }
        />
    )
}

export default privateRoute